package chapter5;

public class Chapter05 {
	public static void main(String[] args) {

		//確認問題

		//「String」型の変数を宣言し、自分の名前を代入
		String name = "Erika";

		//自分の名前を出力
		System.out.println(name);
		
		//「String」型の変数に、好きな言葉を再代入
		name = "人生一度きり";
		
		//好きな言葉を出力
		System.out.println(name);
		
		//「int」型のfinal定数を宣言し、ラッキーナンバーを代入
		final int LUCKY_NUMBER = 421;
		
		//ラッキーナンバーを出力
		System.out.println(LUCKY_NUMBER);

	}
}
